function normalizarForma(valor: string): string {
  return valor
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/\s+/g, ' ')
    .trim()
    .toUpperCase()
}

export function mapearFormaFarmaceutica(valor: string | null): FormaFarmaceutica {
  if (valor === null) return 'OTRO'

  const formaNormalizada = normalizarForma(valor)
  if (formaNormalizada === '') return 'OTRO'

  return FORMAS_DIGEMID[formaNormalizada] ?? 'OTRO'
}
